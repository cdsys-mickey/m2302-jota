import { FlexBox } from "@/shared-components";
import { Typography } from "@mui/material";
import PropTypes from "prop-types";
import { useMemo } from "react";
import { useWatch } from "react-hook-form";

export const D05ProdGridSubtotalLabel = (props) => {
	const { name = "TotAmt_N", ...rest } = props;
	const subtotal = useWatch({
		name,
	});

	const formattedSubtotal = useMemo(() => {
		return !isNaN(subtotal) && subtotal !== ""
			? parseFloat(subtotal).toFixed(2)
			: "0.00";
	}, [subtotal]);

	return (
		<FlexBox inline sx={{ fontWeight: 700 }}>
			合計：
			<Typography color="primary" {...rest}>
				{formattedSubtotal}
			</Typography>
			{/* <FlexBox minWidth="80px" /> */}
		</FlexBox>
	);
};

D05ProdGridSubtotalLabel.propTypes = {
	name: PropTypes.string,
};

D05ProdGridSubtotalLabel.displayName = "D05ProdGridSubtotalLabel";
